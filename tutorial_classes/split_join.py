# assign given statement to a string variable
a = "Python is great"

# split given statement and assign to new split_a variable, split based on whitespaces inbetween.
split_a = a.split(' ')

# join split statement based on whitespaces to get original statement
join_split_a = ' '.join(split_a)

# print
print(a)
print(split_a)
print(join_split_a)